CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_get_images_by_study_number`(in in_study_id bigint unsigned)
begin



    DECLARE done INT DEFAULT FALSE;

    DECLARE dicom_id bigint unsigned;

    DECLARE body_image mediumblob;

    DECLARE dicom_serie varchar(500);





    -- Declarar el cursor y realizar la consulta para obtener los IDs

    DECLARE dicom_ids CURSOR FOR SELECT d_id FROM dicoms WHERE d_ma_id = in_study_id;



    -- Manejo de excepciones en caso de error con el cursor

    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = TRUE;



    -- Crear tabla temporal para almacenar los resultados

    CREATE TEMPORARY TABLE `images_temp`(

        i_id bigint unsigned,

        i_serie varchar(500),

        i_image mediumblob

    );



    -- Abrir el cursor y recorrer los IDs

    OPEN dicom_ids;



    read_loop:

    LOOP

        -- Leer el siguiente ID del cursor

        FETCH dicom_ids INTO dicom_id;



        IF done THEN

            LEAVE read_loop;

        END IF;



        -- Consultar el campo de la otra tabla usando el ID actual

        SELECT i_image, i_d_serie INTO body_image, dicom_serie FROM images WHERE i_d_id = dicom_id;



        -- Insertar el resultado en la tabla temporal

        INSERT INTO images_temp (i_id, i_serie,i_image) VALUES (dicom_id, dicom_serie,body_image);

    END LOOP;



    -- Cerrar el cursor

    CLOSE dicom_ids;



    -- Retornar los campos obtenidos a trav√©s de la tabla temporal

    SELECT * FROM images_temp;



    -- Eliminar la tabla temporal

    DROP TEMPORARY TABLE images_temp;



end ;;
